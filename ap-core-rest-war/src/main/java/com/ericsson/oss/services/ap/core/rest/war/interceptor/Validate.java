package com.ericsson.oss.services.ap.core.rest.war.interceptor;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

import javax.interceptor.InterceptorBinding;

/**
 * Interceptor binding annotation for validating a REST request body. Used on REST endpoint methods to enable {@link ValidateInterceptor} to validate fields
 * in annotated pojo
 */
@Retention(RetentionPolicy.RUNTIME)
@Target({ElementType.TYPE, ElementType.METHOD})
@InterceptorBinding
public @interface Validate {
}
